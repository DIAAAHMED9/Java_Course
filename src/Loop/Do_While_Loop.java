package Loop;

public class Do_While_Loop {
    public static void main(String[] args) {

        int i = 1;
        do {
            System.out.println(i);
            i++;
        } while (i <= 5);


//   infinity do while loop
        int x = 0;
        do {
            System.out.println(x);
            x++;
        } while (x >= 0);
    }
}
